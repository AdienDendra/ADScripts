def createControl( select ='',
                  prefix ='',
                  suffix='ctrl',
                  groupsCtrl =['Zro'],
                  groupConnectAttr =[''],
                  ctrlSize = 1.0,
                  ctrlColor = '',
                  gimbal=None,
                  lockChannels=[''],
                  lockGmblChannels=[''],
                  objVis = None,
                  shape=SQUARE,
                  connect=['']
                  ):

    for obj in mc.ls(select):

        # create main control
        sCtrl = ut.scale_curve(ctrlSize, shape)
        ctrl = ut.create_controller(sCtrl)

        if prefix == '':
            renCtrl = mc.rename(ctrl, '%s_%s' % (ut.ad_lib_main_name(obj), suffix))
            grpPrnt = ut.group_parent(groupsCtrl, '%s' % ut.ad_lib_main_name(obj), suffix.title())

        else:
            renCtrl = mc.rename(ctrl, '%s_%s' % (ut.ad_lib_main_name(prefix), suffix))
            grpPrnt = ut.group_parent(groupsCtrl, '%s' % ut.ad_lib_main_name(prefix), suffix.title())

        parCtrl = ut.parent_object(grpPrnt[-1], renCtrl)
        cnntCtrl = renCtrl

            # gimbal control true
        if gimbal == True:
            # scaling size gimbal from the main ctrl
            sCrv = ut.scale_curve(ctrlSize * 0.75, shape)
            # create gimbal control
            ctrlGmb = ut.create_controller(sCrv)
            if prefix == '':
                renGmbl = mc.rename(ctrlGmb, '%s_%s' % (ut.ad_lib_main_name(obj), ut.GIMBAL))
            else:
                renGmbl = mc.rename(ctrlGmb, '%s_%s' % (ut.ad_lib_main_name(prefix), ut.GIMBAL))

            # gimbal control alias
            cnntCtrl = renGmbl

            # gimbal color
            ut.ad_cc_set_color(renGmbl, 'white')

            # add attribute for switching on/off gimbal ctrl
            addAttr = ut.add_attr_transform_shape(renCtrl, 'gimbalCtrl', 'long', min=0, max=1, dv=0, edit=True, keyable=True)
            lRGmbl = mc.listRelatives(renGmbl, shapes=True)[0]
            mc.connectAttr('%s.gimbalCtrl' % addAttr, '%s.visibility' % lRGmbl)

            # lock and hide attribute
            ut.lock_hide_attr(lockGmblChannels, renGmbl)

            # lock and hide visibility the gimbal ctrl
            ut.lock_hide_attr_object(cnntCtrl, 'visibility')

            # parent gimbal ctrl to main ctrl
            ut.parent_object(renCtrl, renGmbl)

        # object visibility control true
        if objVis == True:
            # add attribute for switching on/off object
            ut.add_attr_transform(renCtrl, 'objVis', 'long', min=0, max=1, dv=1, edit=True, keyable=True)
            mc.connectAttr('%s.objVis' % renCtrl, '%s.visibility' % obj)
            ut.lock_hide_attr_object(obj, 'visibility')

        # match position the group ctrl as the obj
        ut.match_position(obj, grpPrnt[0])

        # match scale the group ctrl as the obj
        ut.match_scale(obj, grpPrnt[0])

        # add attribute message to make easy track down in node editor
        ut.ad_add_attr_message(renCtrl, grpPrnt[0])

        # color control
        ut.ad_cc_set_color(renCtrl, ctrlColor)

        # lock and hide attribute
        ut.lock_hide_attr(lockChannels, renCtrl)

        # lock and hide visibility the main ctrl
        ut.lock_hide_attr_object(renCtrl, 'visibility')

        if connect == ['connectAttr']:
            ut.ad_group_object(groupConnectAttr, obj)
            ut.connection(connect, renCtrl, obj)

        # connection parent
        elif connect == ['parent']:
            # query list relatives
            lrP = mc.listRelatives(obj, p=1)

            if lrP == None:
                ut.connection(connect, cnntCtrl, obj)
            else:
                # parent object to controller
                ut.connection(connect, cnntCtrl, obj)

                # parent ctrl group to list relatives
                ut.parent_object(lrP, grpPrnt[0])


        else:
            # connection constraint
            ut.connection(connect, cnntCtrl, obj)


    return grpPrnt
